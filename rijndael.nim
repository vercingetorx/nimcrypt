proc stateInit(state: var BlockState, key: openArray[uint8], keylen: int): int =
  var Nr: int

  case keylen
  of 16:
    Nr = 10
  of 24:
    Nr = 12
  of 32:
    Nr = 14
  else:
    return ERR_NR_ROUNDS

  state.rounds = Nr
  discard rijndaelKeySetupEnc(state.ek, key, keylen * 8)
  discard rijndaelKeySetupDec(state.dk, key, keylen * 8)
  return 0